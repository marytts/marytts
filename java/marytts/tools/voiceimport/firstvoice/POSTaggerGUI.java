    private void readPOSData(){
        ArrayList<String> lines = new ArrayList<String>(); 
        String line;
        
        // Reading file to ArrayList
        try{
            BufferedReader br = new BufferedReader(new FileReader(fileName));
            while((line = br.readLine()) != null){
               lines.add(line.trim());
            }
            br.close();
        }
        catch(Exception e){
            //System.err.println(e);
            e.printStackTrace();
        }
        
        // Create words list
        wordList = new POSWords[lines.size()];
        Iterator<String> itr = lines.iterator();
        for(int i=0; itr.hasNext() ; i++) {
            wordList[i] = new POSWords(); 
            String element = itr.next();
            String[] wrds = element.split("\\s+");
            
            wordList[i].setWord(wrds[0]);
            wordList[i].setType(wrds[1]);
            if(wrds[2].equals("0"))
            { wordList[i].setInspection(false); }
            else
            { wordList[i].setInspection(true);  }
        }
   }
   
    
   private void writePOSData(){
       try{
           PrintWriter pw = new PrintWriter(new FileWriter(fileName));
           for(int i=0; i<wordList.length; i++){
               int ins;
               if(wordList[i].getInspection()) ins = 1;
               else ins = 0;
               pw.println(wordList[i].getWord()+" "+wordList[i].getType()+" "+ins);
               //System.err.println(wordList[i].getWord()+" "+wordList[i].getType()+" "+wordList[i].getInspection());
               //System.err.println(wordList[i].getWord()+" "+wordList[i].getType()+" "+ins);
           }
  
           pw.flush();
           pw.close();
       }
       catch(Exception e){
           e.printStackTrace();
       }
   } 
   
   private void showData(){
        
        fwList = new POSWordPanel[panelSize];
        panels = new JPanel[8];
        panels[0] = jPanel2;
        panels[1] = jPanel3;
        panels[2] = jPanel4;
        panels[3] = jPanel5;
        panels[4] = jPanel6;
        panels[5] = jPanel7;
        panels[6] = jPanel8;
        panels[7] = jPanel9;
        
        startIndex = findStartIndex();
        endIndex = startIndex + panelSize - 1;
        
        if(startIndex == 0) jButton3.setEnabled(false);
        
        //if(endIndex >= wordList.length){
        //    endIndex = wordList.length - 1;
        //}
        
        for(int i=startIndex,j=0; i <= endIndex ; i++,j++){
            showPanelData(fwList[j],panels[j], i);
        }
    }
   
   private void showPanelData(POSWordPanel fw, JPanel jp, int intVal){
       jp.removeAll();
       if(intVal < wordList.length){
           fw = new POSWordPanel(wordList[intVal]);
           fw.resize(jp.size());
           jp.add(fw, java.awt.BorderLayout.CENTER);
       }
       else{
           jButton2.setEnabled(false);
       }
       jp.updateUI(); //setVisible(true);
       this.setVisible(true);
   }
   
   private int findStartIndex(){
       int index = 0;
       for(int i=0; i<wordList.length; i++){
           if(!wordList[i].getInspection()){
               index = i;
               break;
           }
       }
       return ((int)(index/panelSize)) * panelSize;
       
   }
    
